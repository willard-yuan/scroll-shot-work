import { mkdir, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';

const appId = '6760192003';
const country = 'cn';
const endpoint = `https://itunes.apple.com/lookup?id=${appId}&country=${country}`;
const outputPath = resolve('src/data/appStoreRating.ts');

const fallback = {
	appId,
	country,
	average: null,
	averageDisplay: 'App Store 评分',
	count: 0,
	countDisplay: '暂无评分数据',
	source: endpoint,
	updatedAt: new Date().toISOString(),
};

const toRating = (payload) => {
	const item = payload?.results?.[0];
	if (!item) {
		return fallback;
	}

	const average =
		typeof item.averageUserRating === 'number'
			? item.averageUserRating
			: typeof item.averageUserRatingForCurrentVersion === 'number'
				? item.averageUserRatingForCurrentVersion
				: null;
	const count =
		typeof item.userRatingCount === 'number'
			? item.userRatingCount
			: typeof item.userRatingCountForCurrentVersion === 'number'
				? item.userRatingCountForCurrentVersion
				: 0;

	return {
		appId,
		country,
		average,
		averageDisplay: average ? average.toFixed(1) : 'App Store 评分',
		count,
		countDisplay: count > 0 ? `${count.toLocaleString('zh-CN')} 个评分` : '暂无评分数据',
		source: endpoint,
		updatedAt: new Date().toISOString(),
	};
};

const run = async () => {
	let rating = fallback;

	try {
		const response = await fetch(endpoint, {
			headers: {
				accept: 'application/json',
			},
		});

		if (!response.ok) {
			throw new Error(`Lookup failed: ${response.status} ${response.statusText}`);
		}

		rating = toRating(await response.json());
	} catch (error) {
		console.warn(`[app-store-rating] ${error.message}`);
	}

	const content = `// Generated by scripts/update-app-store-rating.mjs.
// Source: ${endpoint}
export const appStoreRatings = ${JSON.stringify({ zh: rating }, null, '\t')} as const;
`;

	await mkdir(dirname(outputPath), { recursive: true });
	await writeFile(outputPath, content);
	console.log(`[app-store-rating] wrote ${outputPath}`);
};

await run();
