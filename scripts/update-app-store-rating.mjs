import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';

const appId = '6760192003';
const outputPath = resolve('src/data/appStoreRating.ts');

const ratingTargets = [
	{
		key: 'zh',
		country: 'cn',
		emptyRatingLabel: 'App Store 评分',
		emptyCountLabel: '暂无评分数据',
		countFormatter: (count) => `${count.toLocaleString('zh-CN')} 个评分`,
	},
	{
		key: 'ja',
		country: 'jp',
		emptyRatingLabel: 'App Store 評価',
		emptyCountLabel: '評価データなし',
		countFormatter: (count) => `${count.toLocaleString('ja-JP')}件の評価`,
	},
];

const endpointFor = (country) => `https://itunes.apple.com/lookup?id=${appId}&country=${country}`;

const fallbackFor = (target) => ({
	appId,
	country: target.country,
	average: null,
	averageDisplay: target.emptyRatingLabel,
	count: 0,
	countDisplay: target.emptyCountLabel,
	source: endpointFor(target.country),
	updatedAt: new Date().toISOString(),
});

const readExistingRatings = async () => {
	try {
		const source = await readFile(outputPath, 'utf8');
		const match = source.match(/export const appStoreRatings = ([\s\S]*?) as const;/);
		return match ? JSON.parse(match[1]) : {};
	} catch {
		return {};
	}
};

const toRating = (target, payload) => {
	const item = payload?.results?.[0];
	if (!item) {
		return fallbackFor(target);
	}

	const average =
		typeof item.averageUserRating === 'number'
			? item.averageUserRating
			: typeof item.averageUserRatingForCurrentVersion === 'number'
				? item.averageUserRatingForCurrentVersion
				: null;
	const count =
		typeof item.userRatingCount === 'number'
			? item.userRatingCount
			: typeof item.userRatingCountForCurrentVersion === 'number'
				? item.userRatingCountForCurrentVersion
				: 0;

	return {
		appId,
		country: target.country,
		average,
		averageDisplay: average ? average.toFixed(1) : target.emptyRatingLabel,
		count,
		countDisplay: count > 0 ? target.countFormatter(count) : target.emptyCountLabel,
		source: endpointFor(target.country),
		updatedAt: new Date().toISOString(),
	};
};

const run = async () => {
	const existingRatings = await readExistingRatings();
	const ratings = {};

	for (const target of ratingTargets) {
		const endpoint = endpointFor(target.country);

		try {
			const response = await fetch(endpoint, {
				headers: {
					accept: 'application/json',
				},
			});

			if (!response.ok) {
				throw new Error(`Lookup failed: ${response.status} ${response.statusText}`);
			}

			ratings[target.key] = toRating(target, await response.json());
			console.log(
				`[app-store-rating] ${target.country}: ${ratings[target.key].averageDisplay}, ${ratings[target.key].countDisplay}`,
			);
		} catch (error) {
			console.warn(`[app-store-rating] ${target.country}: ${error.message}`);
			ratings[target.key] = existingRatings[target.key] ?? fallbackFor(target);
		}
	}

	const sources = ratingTargets.map((target) => endpointFor(target.country)).join(', ');
	const content = `// Generated by scripts/update-app-store-rating.mjs.
// Sources: ${sources}
export const appStoreRatings = ${JSON.stringify(ratings, null, '\t')} as const;
`;

	await mkdir(dirname(outputPath), { recursive: true });
	await writeFile(outputPath, content);
	console.log(`[app-store-rating] wrote ${outputPath}`);
};

await run();
